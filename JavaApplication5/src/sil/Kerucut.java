package sil;

public class Kerucut {
   public double pi = 3.14, r, s;

 
   
    public double Volume(double r, double t){
        return (pi*r*r*t)/3;
    }
    public double Luas(double r, double s){
        double lalas = pi*r*r;
        double lselimut = pi*r*s;
        return lalas+lselimut;
    } 
}
